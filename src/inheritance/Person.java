package inheritance;

/**
 *
 * @author fachr
 */
public abstract class Person {
    
    private String nama;
    private String jk;
    
    public abstract String showPerson();

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getJk() {
        return jk;
    }

    public void setJk(String jk) {
        this.jk = jk;
    }

    
    
    
}
